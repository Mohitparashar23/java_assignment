class Squarecube{
public static void main (String[] args)
{
// Square of a number (Sqr of4, Sqr of 7, Sqr of 9)

int a = 4;
int sqrt4 = a*a;
int b = 7;
int sqrt7 = b*b;
int c = 9;
int sqrt9 = c*c;

System.out.println("Square of 4 = " +sqrt4);
System.out.println("Square of 7 =" +sqrt7);
System.out.println("Square of 9 =" +sqrt9);

//Cube of a number (Cube of 5,cube of 2,cube of 15);

int m = 5;
int cube5 = m*m*m;
int n = 2;
int cube2 = n*n*n;
int o = 15;
int cube15 = o*o*o;

System.out.println("Cube of 5 =" +cube5);
System.out.println("Cube of 2 =" +cube2);
System.out.println("Cube of 15 =" +cube15);

}
}